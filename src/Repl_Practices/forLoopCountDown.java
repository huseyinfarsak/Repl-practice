package Repl_Practices;

public class forLoopCountDown {
    public static void main(String[] args) {



        for (int i = 100 ; i>=0 ; i-- ){    // 100 den geri yazdirma
                                           // int i = 0 ; i<=100 ; i++  yaparsak 0 dan 10 e sayar
            System.out.print(" " +i);
        }
    }
}
